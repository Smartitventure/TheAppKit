<h4>Create an App store account</h4>

<p>In order to get your App in the store you will need a developer account for each store (Apple & Android). Please follow the steps below to create an account</p>


<h3>Apple Developer Account</h3>


<p>Click the link below and follow these simple steps</p>
<a href="https://developer.apple.com/programs/enroll/">https://developer.apple.com/programs/enroll/</a>
<ul>
<li> 1) Go to the Apple Developer Program enrollment page and click Start Your Enrollment.</li>

<li> 2)Log in with your Apple ID and password.</li>

<li> 3) When prompted to verify your identity, enter the verification code sent to your Apple ID email address and click Continue.</li>

<li> 4) From the Entity Type list, select Company/Organization and click Continue.</li>

<li> 5) Under Authority to Sign Legal Agreements, select the appropriate option:</li>

<ul>
<li>- I am the owner/founder and have the authority to bind my organization to legal agreements –  Upon selection, Your Work Email field will be displayed. Enter your business email address; you must use a private domain name (e.g., email@yourbusiness.com). You cannot use Gmail, Hotmail, Yahoo, or any other email addresses from common email service providers.</li>

<li>- My organization has given me the authority to bind it to legal agreements  –  Apple will contact a senior employee at your organization to confirm enrollment. Under Verification Contact enter the employee’s contact information.</li>
</ul>

<li> 6) Under Organization Information, enter the information for your organization:</li>
<ul>
<li> a) In the Legal Entity Name field, enter the name of your business as it appears in the D-U-N-S lookup. Your enrollment will not be successful if the legal entity name does not match the one in the D&B profile.</li>

<li> b) In the D-U-N-S Number field, enter the nine-digit D-U-N-S number assigned to your business. For more information on retrieving your D-U-N-S Number, see What is a D-U-N-S Number?</li>

<li> c) In the Website field, enter the website URL of your organization’s publicly available website with a domain name associated with your organization.</li>

<li> d)In the Headquarters Phone field, enter a phone number where Apple can reach you to confirm enrollment. Apple will call the number provided in this field to verify your enrollment.</li>

<li> e)You can leave the Tax ID/National ID field blank.</li>

<li> f) Type the correct characters in the CAPTCHA and click Continue.
Note - If you receive an error message under the Legal Entity Name field, click update your D&B profile and submit your business’s information. You may continue this procedure once your profile has been updated.</li>
</ul>
</ul>
<br><br><br><br>

<h3>On the Summary for Review page, review and confirm the information. </h3>

<p>After submitting your application, Apple will contact you to verify your enrollment. A confirmation email will be sent to your Apple ID email address with a link enclosed to confirm and pay for your developer membership. The $99 (USD) membership is billed annually.</p>
