     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request,[
            'app_details'=>'required', 'string', 'max:255',
            'about_app'=>'required', 'string', 'max:255',
        ]);
        $data = array();
        $data['app_details'] = $request->app_details;
        $data['about_app'] = $request->about_app;

        $notify = Publish::create($data);

        session::flash('statuscode','info');
        return redirect('pushnotification')->with('status','Data is Added');
    }

